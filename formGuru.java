    private void HapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusActionPerformed
        // TODO add your handling code here:
        int del = JOptionPane.showConfirmDialog(this, "Apa anda yakin akan menghapus ?", "Hapus", JOptionPane.OK_CANCEL_OPTION);
        if(del == JOptionPane.YES_OPTION) {
            int i = jTable1.getSelectedRow();
            data.guru.remove(i);
            JOptionPane.showMessageDialog(this, "Sukses Hapus Data !", "Sukses", JOptionPane.DEFAULT_OPTION);  
        } else {
            JOptionPane.showMessageDialog(this, "Batal Menghapus Data !", "Batal", JOptionPane.CANCEL_OPTION);
        }
        data.simpanGuru();
        reset();
    }//GEN-LAST:event_HapusActionPerformed

