    public void tampilData(){
    DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
    for (Guru elem : data.guru) {
        String[] data = new String[4];
        data[0] = elem.getNama();
        data[1] = elem.getNIK();
        data[2] = elem.getJabatan();
        data[3] = elem.getAlamat();
        dtm.addRow(data);
    }
    buttonManage(true);
    }

    public void buttonManage(boolean param){
        if(param){
            Tambah.setEnabled(true);
            Edit.setEnabled(true);
            Hapus.setEnabled(true);
            nik.setEnabled(true);
        } else {
            Tambah.setEnabled(false);
            Edit.setEnabled(false);
            Hapus.setEnabled(false);
            nik.setEnabled(false);
        }
    }

    public void reset(){
        nama.setText("");
        nik.setText("");
        jabatan.setText("");
        alamat.setText("");
        buttonManage(true);
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int rowList = dtm.getRowCount();
        for (int i = rowList - 1; i >= 0; i--) {
            dtm.removeRow(i);
        }
        tampilData();
    }

