    private void TambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahActionPerformed
        // TODO add your handling code here:
        data.guru.add(new Guru(nama.getText(), nik.getText(), jabatan.getText(), alamat.getText()));
        JOptionPane.showMessageDialog(this, "Berhasil Tambah Data !", "Sukses", JOptionPane.DEFAULT_OPTION);
        data.simpanGuru();
        reset();
    }//GEN-LAST:event_TambahActionPerformed

    private void EditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditActionPerformed
        // TODO add your handling code here:
        int up = jTable1.getSelectedRow();
        data.guru.get(up).setNama(nama.getText());
        data.guru.get(up).setNIK(nik.getText());
        data.guru.get(up).setJabatan(jabatan.getText());
        data.guru.get(up).setAlamat(alamat.getText());
        data.simpanGuru();
        JOptionPane.showMessageDialog(this, "Sukses Update Data !", "Sukses", JOptionPane.DEFAULT_OPTION);
        reset();
    }//GEN-LAST:event_EditActionPerformed

